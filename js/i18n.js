  let current = localStorage.getItem("darts-lang") || "en";
  if (!LANGS[current]) current = "en";

  function t(key) {
    const v = LANGS[current][key];
    return v !== undefined ? v : (LANGS.en[key] !== undefined ? LANGS.en[key] : key);
  }

  // Replaces {a}, {b}, {n}, {d} placeholders in a template string
  function fmt(str, vars) {
    return str.replace(/\{(\w+)\}/g, (m, k) => (vars[k] !== undefined ? vars[k] : m));
  }

  // Updates every element tagged with data-i18n / data-i18n-ph
  function apply() {
    document.documentElement.lang = current;
    document.title = t("title");
    document.querySelectorAll("[data-i18n]").forEach(el => { el.textContent = t(el.dataset.i18n); });
    document.querySelectorAll("[data-i18n-ph]").forEach(el => { el.placeholder = t(el.dataset.i18nPh); });
    document.querySelectorAll(".lang-btn").forEach(b => b.classList.toggle("active", b.dataset.lang === current));
    // show the current language's flag on the dropdown toggle
    const flag = document.querySelector('.lang-btn[data-lang="' + current + '"] .flag');
    const holder = document.getElementById("lang-current-flag");
    if (flag && holder) holder.innerHTML = flag.outerHTML;
  }

  function setLang(lang) {
    if (!LANGS[lang]) return;
    current = lang;
    localStorage.setItem("darts-lang", lang);
    apply();
  }

  return { t, fmt, apply, setLang, get lang() { return current; } };
